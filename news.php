    <h3>Royal Visit to Bear Cottage</h3>
    <hr>
    <p>The visit by the Duke and Duchess of Cambridge was eagerly anticipated by all at Bear Cottage and it was
        certainly worth the wait.

        The royal couple were warmly welcomed by patients, families, staff and volunteers and witnessed first-hand some
        of the vital work of Bear Cottage caring for children with life-limiting illnesses and their families.

        The Duchess was given an insight into the extensive use of therapy to care for patients and families,
        participating in play therapy and music therapy sessions. There were some touching moments as Kate interacted
        with the children who were in awe of this real-life princess.

        The Duke met with some of the bereaved families who have been touched by the work of Bear Cottage. The families
        appreciated him taking the time to learn about their journeys and offering them kind and comforting words.

        The couple were presented with a unique artwork created using the fingerprints and foot prints of children,
        siblings, parents, staff and volunteers at Bear Cottage. This touching tribute represented that there is no
        footprint too small to leave an imprint on the world.

        Bear Cottage is very grateful to the Duke and Duchess of Cambridge for taking the time out of their busy
        schedules to visit.

        Click here to learn more about helping Bear Cottage care for the most precious children in our community. Your
        donation will help provide around-the-clock nursing and medical care and will also fund programs designed to
        help families to make treasured memories together and gain emotional support during their time of greatest
        need.</p>

    <h3>Bandaged Bear Appeal 2014</h3>
    <hr>
    Thank you to all everyone who supported the Bandaged Bear Appeal in March for The Children’s Hospital at Westmead.
    Bandaged Bear was delighted to see he has so many supporters and friends.

    The Bandaged Bear Appeal is the signature fundraiser for The Children’s Hospital at Westmead with schools and
    community groups rallying their support. Thousands of people were involved holding special events and making
    donations to raise much needed funds for sick children and their families.

    The inaugural ‘Grin and Bear It Challenge’ saw our most dedicated supporters tackle a six-kilometre mud run and
    other obstacles as part of the Raw Challenge.

    Social media also helped to spread the word with celebrities proudly sharing photos of themselves with Bandaged Bear
    or their own special bears. To make a donation, please click here.

